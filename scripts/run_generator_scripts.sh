#!/bin/sh

################################################################################

build_dirpath="" # e.g. `.`

################################################################################
# Generator
################################################################################

check_settings()
{
  if [ -z "${build_dirpath}" ]; then
    echo "Error: 'build_path' is invalid..." >&2
    exit 1
  fi
}

################################################################################

main()
(
  check_settings

  # Run
  ninja -C "${build_dirpath}" libcxx-generate-files
)

################################################################################

main "$@"
